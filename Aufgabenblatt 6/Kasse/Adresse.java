import java.util.ArrayList;

public class Adresse {
  String event;
  String host;
  String street;
  String nr;
  String postal;
  String city;

  public Adresse(String event, String host, String street,
              String nr, String postal, String city) {
    this.event = event;
    this.host = host;
    this.street = street;
    this.nr = nr;
    this.postal = postal;
    this.city = city;
  }
}
